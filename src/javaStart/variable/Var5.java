package javaStart.variable;

public class Var5 {
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);

        //변수 선언, 초기화 한번에 가능
        int b = 2;
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
